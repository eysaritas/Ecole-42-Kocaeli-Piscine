void	ft_rev_int_tab(int *tab, int size)
{
	int	start;
	int	end;
	int	temp;

	end = size - 1;
	start = 0;
	temp = 0;
	while (start < end)
	{
		temp = tab[start];
		tab[start] = tab[end];
		tab[end] = temp;
		start++;
		end--;
	}
}
